package com.diego.modul4;


public class User {

    private int id;
    private String namaLengkap;
    private String tglLahir;
    private String umur;
    private String gender;
    private String service;



    public int getId() {return id;}

    public void setId(int id) {this.id =id; }

    public String getNamaLengkap() { return namaLengkap;}

    public void setNamaLengkap(String namaLengkap) { this.namaLengkap = namaLengkap;}

    public String getTglLahir() { return tglLahir;}

    public void setTglLahir(String tglLahir) { this.tglLahir = tglLahir;}

    public String getUmur() { return umur;}

    public void setUmur(String umur) { this.umur = umur;}

    public String getGender() { return gender;}

    public void setGender(String gender) { this.gender = gender;}

    public String getService() { return service;}

    public void setService(String service) { this.service = service;}

}
